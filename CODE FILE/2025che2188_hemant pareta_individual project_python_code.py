def plot_figure2(G, avalanche_sizes, tumour_counts, state):

    fig, axes = plt.subplots(1, 3, figsize=(15, 4.8))
    fig.suptitle("SOC Signatures in the Tumour Network Model",
                 fontsize=12, fontweight='bold')

    # ── (Left) CCDF ──────────────────────────────────────────────
    ax = axes[0]
    s_sorted, ccdf = compute_ccdf(avalanche_sizes)
    ax.loglog(s_sorted, np.clip(ccdf, 1e-4, None),
              'r-|', lw=1.5, ms=6, markeredgewidth=1.2)
    ax.set_xlabel("Cascade size s", fontsize=9)
    ax.set_ylabel("P(S ≥ s)", fontsize=9)
    ax.set_title("CCDF of cascade sizes\n(power-law tail = SOC)", fontsize=9)
    ax.grid(True, which='both', alpha=0.25)

    # ── (Centre) Degree distribution of tumour sub-network ───────
    ax = axes[1]
    cancer_nodes = [v for v in G.nodes() if state[v] == CANCEROUS]
    if cancer_nodes:
        deg_seq          = [G.degree(v) for v in cancer_nodes]
        unique_k, cnt_k  = np.unique(deg_seq, return_counts=True)
        prob_k           = cnt_k / cnt_k.sum()
        ax.loglog(unique_k.astype(float), prob_k, 's',
                  color='navy', ms=5, alpha=0.8)
        # Power-law visual guide
        k_fit  = np.linspace(1, float(unique_k.max()), 100)
        ax.loglog(k_fit, k_fit**-3 / k_fit[0]**-3 * prob_k[0],
                  'r--', lw=1.2, label='k⁻³ guide', alpha=0.6)
        ax.legend(fontsize=8)
    ax.set_xlabel("Degree k", fontsize=9)
    ax.set_ylabel("P(k)", fontsize=9)
    ax.set_title("Degree distribution of tumour\nsub-network (scale-free?)", fontsize=9)
    ax.grid(True, which='both', alpha=0.25)

    # ── (Right) Rolling Hurst exponent ───────────────────────────
    ax = axes[2]
    H_vals = rolling_hurst(tumour_counts, window=40)
    ax.plot(np.arange(len(H_vals)), H_vals,
            color='steelblue', lw=1.3, alpha=0.85)
    ax.axhline(0.5, color='dimgray', ls='--', lw=1.3, label='H=0.5 (random)')
    h_mean = float(np.mean(H_vals))
    ax.axhline(h_mean, color='firebrick', ls=':', lw=1.2,
               label=f'H̄={h_mean:.2f}')
    ax.set_ylim(0.35, 1.05)
    ax.set_xlabel("Window index", fontsize=9)
    ax.set_ylabel("H", fontsize=9)
    ax.set_title("Rolling Hurst exponent\n(window=40 steps)", fontsize=9)
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.25)

    plt.tight_layout()
    save_path = os.path.join(OUT_DIR, "figure2_soc_signatures.png")
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"  ✓ Figure 2 saved → {save_path}")
    plt.close()


