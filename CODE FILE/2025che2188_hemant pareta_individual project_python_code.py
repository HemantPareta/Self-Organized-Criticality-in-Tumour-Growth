# GLOBAL PARAMETERS  (Table 1 in the report)
# ─────────────────────────────────────────────
SEED           = 13
N              = 300        # number of cell-nodes
M_BA           = 2          # BA attachment parameter
THETA          = 5          # mutation threshold (multi-hit)
MU0            = 0.04       # base mutation rate
SIGMA          = 0.02       # noise std (stochastic mutagen spikes)
RHO            = 0.25       # recruitment (cascade) probability
N_SEEDS        = 3          # initial pre-cancerous cells (mi = θ-1)
T_MAX          = 2500       # max Monte-Carlo steps
NECROSIS_RATE  = 0.0        # 0 for pure invasion (growth phase)

# Percolation analysis
PERC_N         = 200
PERC_REPS      = 25
PERC_THRESHOLD = 0.40       # fraction of failed neighbours to trigger cascade
K_VALUES       = np.arange(1, 11, 0.5)

# Cell state constants
HEALTHY   = 0
CANCEROUS = 1
NECROTIC  = 2

rng = np.random.default_rng(SEED)


# ═══════════════════════════════════════════════
# 1.  BUILD BARABÁSI–ALBERT TISSUE NETWORK
# ═══════════════════════════════════════════════
def build_tissue_network(n=N, m=M_BA, seed=SEED):
    """Return a BA preferential-attachment graph representing the tissue."""
    G = nx.barabasi_albert_graph(n, m, seed=seed)
    return G


# ═══════════════════════════════════════════════
# 2.  TUMOUR GROWTH SIMULATION
# ═══════════════════════════════════════════════
def run_simulation(G):
    """
    Monte-Carlo simulation of tumour invasion.

    Returns
    -------
    tumour_counts   : (T, 2) array  — [time, #cancerous]
    avalanche_sizes : 1-D array     — size of each cascade event
    mean_degrees    : (T, 2) array  — [time, mean_degree_of_tumour_subgraph]
    state           : (N,) array    — final cell states
    final_mutation  : (N,) array    — final mutation loads
    G               : networkx Graph
    """
    n = G.number_of_nodes()
    state    = np.full(n, HEALTHY, dtype=int)
    mutation = np.zeros(n, dtype=int)

    # Seed 3 pre-cancerous cells at mutation load θ-1
    seed_nodes = rng.choice(n, N_SEEDS, replace=False)
    mutation[seed_nodes] = THETA - 1

    tumour_counts   = []
    avalanche_sizes = []
    mean_degrees    = []
    cancerous_set   = set()

    # ── BFS recruitment cascade ──
    def run_cascade(initial_wave):
        wave = list(initial_wave)
        total_converted = list(wave)
        for node in wave:
            state[node] = CANCEROUS
            cancerous_set.add(node)

        while wave:
            next_wave = []
            for c_node in wave:
                for nbr in G.neighbors(c_node):
                    if state[nbr] == HEALTHY:
                        if rng.random() < RHO:
                            state[nbr] = CANCEROUS
                            cancerous_set.add(nbr)
                            next_wave.append(nbr)
                            total_converted.append(nbr)
            wave = next_wave
        return len(total_converted)

    # ── Main MC loop ──
    for t in range(T_MAX):

        # STEP 1 — Mutation accumulation (slow drive)
        new_wave = []
        for i in range(n):
            if state[i] == HEALTHY:
                p_mut = MU0 + abs(rng.normal(0, SIGMA))
                if rng.random() < p_mut:
                    mutation[i] += 1
                    if mutation[i] >= THETA:
                        new_wave.append(i)

        # STEP 2 — Recruitment cascade (fast relaxation)
        if new_wave:
            size = run_cascade(new_wave)
            avalanche_sizes.append(size)

        # STEP 3 — Record observables
        n_cancer = int(np.sum(state == CANCEROUS))
        tumour_counts.append([t, n_cancer])

        if cancerous_set:
            sub_deg = [G.degree(v) for v in cancerous_set]
            mean_degrees.append([t, float(np.mean(sub_deg))])
        else:
            mean_degrees.append([t, 0.0])

        if n_cancer == n:          # full invasion — stop
            break

    return (np.array(tumour_counts),
            np.array(avalanche_sizes, dtype=float),
            np.array(mean_degrees),
            state.copy(),
            mutation.copy(),
            G)


