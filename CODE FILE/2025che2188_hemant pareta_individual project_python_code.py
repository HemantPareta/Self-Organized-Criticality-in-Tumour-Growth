# 3.  PERCOLATION ANALYSIS  (Section 3.3)
# ═══════════════════════════════════════════════
def run_percolation_analysis():
    """
    Erdős–Rényi threshold cascade: sweep k from 1 → 10,
    record mean cascade fraction over PERC_REPS realisations.
    """
    cascade_fractions = []
    for k_target in K_VALUES:
        fracs = []
        for _ in range(PERC_REPS):
            p_edge = k_target / (PERC_N - 1)
            G_er   = nx.erdos_renyi_graph(PERC_N, p_edge,
                                          seed=int(rng.integers(1_000_000)))
            failed  = set(range(3))
            changed = True
            while changed:
                changed = False
                for node in range(PERC_N):
                    if node in failed:
                        continue
                    nbrs = list(G_er.neighbors(node))
                    if not nbrs:
                        continue
                    f_frac = sum(1 for nb in nbrs if nb in failed) / len(nbrs)
                    if f_frac >= PERC_THRESHOLD:
                        failed.add(node)
                        changed = True
            fracs.append(len(failed) / PERC_N)
        cascade_fractions.append(np.mean(fracs))
    return np.array(cascade_fractions)


