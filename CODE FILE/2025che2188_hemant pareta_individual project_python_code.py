# 9.  FIGURE 3 — Network final state
# ═══════════════════════════════════════════════
def plot_figure3(G, state):

    COLOR = {HEALTHY: 'limegreen', CANCEROUS: 'firebrick', NECROTIC: 'dimgray'}
    LABEL = {HEALTHY: 'Healthy',   CANCEROUS: 'Cancerous', NECROTIC: 'Necrotic'}

    node_colors = [COLOR[state[v]] for v in G.nodes()]
    degrees     = np.array([G.degree(v) for v in G.nodes()])
    node_sizes  = 18 + 3.5 * degrees   # hubs appear larger

    pos = nx.spring_layout(G, seed=SEED, k=0.32)

    fig, ax = plt.subplots(figsize=(10, 8.5))
    fig.patch.set_facecolor('white')

    # Edges (very transparent)
    nx.draw_networkx_edges(G, pos, ax=ax,
                           alpha=0.07, edge_color='gray', width=0.5)

    # Nodes — draw by state so legend works cleanly
    for s_val in [HEALTHY, CANCEROUS, NECROTIC]:
        nlist = [v for v in G.nodes() if state[v] == s_val]
        sizes = [node_sizes[v] for v in nlist]
        nx.draw_networkx_nodes(G, pos, nodelist=nlist,
                               node_color=COLOR[s_val],
                               node_size=sizes, ax=ax,
                               label=LABEL[s_val], alpha=0.88)

    ax.legend(loc='upper left', framealpha=0.9, fontsize=10, markerscale=1.3)
    ax.set_title("Tumour network: final state\n(Barabási–Albert topology)",
                 fontsize=12, fontweight='bold')
    ax.axis('off')

    plt.tight_layout()
    save_path = os.path.join(OUT_DIR, "figure3_network_final_state.png")
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"  ✓ Figure 3 saved → {save_path}")
    plt.close()


