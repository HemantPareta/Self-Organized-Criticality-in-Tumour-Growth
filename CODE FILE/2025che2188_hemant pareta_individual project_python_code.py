# 4.  HURST EXPONENT  (R/S analysis)
# ═══════════════════════════════════════════════
def hurst_rs(ts):
    """Compute Hurst exponent via Rescaled Range (R/S) analysis."""
    ts = np.asarray(ts, dtype=float)
    n  = len(ts)
    if n < 8:
        return 0.5
    lags     = np.unique(np.geomspace(8, max(8, n // 2), num=20).astype(int))
    rs_vals  = []
    lag_vals = []
    for lag in lags:
        chunks   = [ts[i:i+lag] for i in range(0, n - lag + 1, lag)]
        rs_chunk = []
        for chunk in chunks:
            mean_c = np.mean(chunk)
            dev    = np.cumsum(chunk - mean_c)
            R, S   = dev.max() - dev.min(), np.std(chunk, ddof=1)
            if S > 0:
                rs_chunk.append(R / S)
        if rs_chunk:
            rs_vals.append(np.mean(rs_chunk))
            lag_vals.append(lag)

    if len(lag_vals) < 2:
        return 0.5
    slope, *_ = linregress(np.log(lag_vals), np.log(rs_vals))
    return float(slope)


def rolling_hurst(tumour_counts, window=40):
    """Rolling Hurst exponent over the tumour growth increment series."""
    diffs  = np.diff(tumour_counts[:, 1].astype(float))
    H_vals = [hurst_rs(diffs[i:i+window])
               for i in range(max(1, len(diffs) - window + 1))]
    return np.array(H_vals)


# ═══════════════════════════════════════════════
# 5.  POWER-LAW FIT
# ═══════════════════════════════════════════════
def fit_power_law(sizes):
    unique, counts = np.unique(sizes.astype(int), return_counts=True)
    prob           = counts / counts.sum()
    log_s, log_p   = np.log10(unique.astype(float)), np.log10(prob.astype(float))
    slope, intercept, r, *_ = linregress(log_s, log_p)
    return slope, intercept, r**2, unique, prob


# ═══════════════════════════════════════════════
# 6.  CCDF
# ═══════════════════════════════════════════════
def compute_ccdf(sizes):
    s = np.sort(sizes.astype(float))
    c = 1.0 - np.arange(1, len(s)+1) / len(s)
    return s, c


